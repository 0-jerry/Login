import UIKit.UIFont

extension UIFont {
    enum System {
        static let regular12 = UIFont.systemFont(ofSize: 12, weight: .regular)
        static let medium20 = UIFont.systemFont(ofSize: 20, weight: .medium)
        static let bold28 = UIFont.systemFont(ofSize: 28, weight: .bold)
    }
}
